export function getRestroomById(id) {
  return mockRestrooms.find(r => r.id === id)
}

export function getReviewsForRestroom(id) {
  return mockReviews.filter(r => r.restroomId === id)
}

export function formatDistance(meters) {
  if (meters < 1000) return `${meters}m`
  return `${(meters / 1000).toFixed(1)}km`
}

export function getMarkerType(restroom) {
  if (restroom.amenities.wheelchair) return 'accessible'
  if (restroom.amenities.genderNeutral) return 'gender-neutral'
  if (restroom.amenities.familyRoom) return 'family'
  return 'default'
}
